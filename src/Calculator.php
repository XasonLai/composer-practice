<?php

namespace Guagualai\Practice;

/**
 * 計算兩個數字的和
 */
class Calculator
{
    /**
     * Returns the sum of two numbers.
     *
     * @param float $a The first number.
     * @param float $b The second number.
     *
     * @return float The sum of the two numbers.
     */
    public function add(float $a, float $b): float
    {
        return $a + $b;
    }
}
